    public DataTable CreateDataSource()
    {
      try
      {

        var list = SetList();

        // 列定義
        var dt = new DataTable();
        dt.Columns.Add("ID", typeof(int));
        dt.Columns.Add("国名", typeof(string));
        dt.Columns.Add("値１", typeof(int));
        dt.Columns.Add("値２", typeof(int));
        dt.Columns.Add("値３", typeof(int));
        dt.Columns.Add("値４", typeof(int));
        dt.Columns.Add("値５", typeof(int));
        dt.Columns.Add("値６", typeof(int));
        dt.Columns.Add("値７", typeof(int));
        dt.Columns.Add("値８", typeof(int));
        dt.Columns.Add("値９", typeof(int));

        foreach (var item in list)
        {
          DataRow row = dt.NewRow();
          row["ID"] = item.Id;
          row["国名"] = item.Name;
          row["値１"] = item.Value1;
          row["値２"] = item.Value2;
          row["値３"] = item.Value3;
          row["値４"] = item.Value4;
          row["値５"] = item.Value5;
          row["値６"] = item.Value6;
          row["値７"] = item.Value7;
          row["値８"] = item.Value8;
          row["値９"] = item.Value9;
          dt.Rows.Add(row);
        }

        return dt;
      }
      catch
      {
        throw;
      }
    }

